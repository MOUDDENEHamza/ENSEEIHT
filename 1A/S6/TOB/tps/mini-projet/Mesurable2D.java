/** Définir les propriétés d'un élément fermé du plan.
 * @author  Xavier Crégut
 * @version 1.3
 */
public interface Mesurable2D {

	/** Obtenir le perimètre de l'élément.
	 * @return le perimètre
	 */
	double perimetre();

	/** Obtenir l'aire de l'élément.
	 * @return l'aire
	 */
	double aire();

}
