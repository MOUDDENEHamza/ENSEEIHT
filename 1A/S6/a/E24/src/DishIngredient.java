/**
 * Dish ingredient to help managing the stock 
 * @author WISSAD
 *
 */
public class DishIngredient {
	private Ingredient ingredient ;
	private int  quantity ;
	
	/**
	 * constructor using ingredient and quantity 
	 * @param ingr
	 * @param quant
	 */
	public DishIngredient(Ingredient ingr, int quant){
		ingredient = ingr;
		quantity = quant;
	}
	/**
	 * Default construtor 
	 */
	public DishIngredient(){}
	
	/**
	 * 
	 * @return ingredient 
	 */
	public Ingredient getIngredient(){
		return ingredient;
	}
	/**
	 * 
	 * @return  quantity 
	 */
	public int getQuantity(){
		return quantity;
	}
	/**
	 * set the quantity 
	 * @param quant
	 */
	public void setQuantity(int quant){
		quantity = quant;
	}
	/**
	 * set the Ingredient 
	 * @param ing
	 */
	public void setIngredient(Ingredient ing){
		ingredient = ing;
	}

}
